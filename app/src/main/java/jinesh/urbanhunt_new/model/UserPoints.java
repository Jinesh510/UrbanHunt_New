public class UserPoints {

    int points_spent;
    int points_earned;
    int points_left;


    public int getPoints_spent() {
        return points_spent;
    }

    public void setPoints_spent(int points_spent) {
        this.points_spent = points_spent;
    }

    public int getPoints_earned() {
        return points_earned;
    }

    public void setPoints_earned(int points_earned) {
        this.points_earned = points_earned;
    }

    public int getPoints_left() {
        return points_left;
    }

    public void setPoints_left(int points_left) {
        this.points_left = points_left;
    }
}
